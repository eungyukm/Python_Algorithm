# 기본 string 표현 " "로 감싸기
print("Hello, Python!")
# Hello, Python!

# ' '로 감싸기
print('Hello, Python!')
# Hello, Python!

# 문자열 안에 따옴표, 쌍따옴표 사용
print("Hello, 'Python!'")
# Hello, 'Python!'

# 문자열 안에 따옴표, 쌍따옴표 사용
print('Hello, "Python!"')
# Hello, "Python!"

# 이스케이프 문자 \\
print("\\'이스케이프\\")
# \'이스케이프\

# 이스케이프 문자 \n
print("줄바꿈\n연습")
# 줄바꿈
# 연습

# 이스케이프 문자 \t
print("탭\t연습")